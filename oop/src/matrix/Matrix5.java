public class Matrix5 {
	public static void main(String[] args) {
		int k = 1;
		for (int i = 0; i < 3; i++) {
			for (int j = 0; j < 5; j++) {
				System.out.print(k+"\t");
				k++;
			}
			System.out.println();
		}
	}
}
